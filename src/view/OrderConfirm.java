    private void tblPromotionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPromotionMouseClicked
        int selectedRow = tblPromotion.getSelectedRow();
        if (selectedRow != -1) {
            // Lấy giá trị giảm giá từ cột thứ 3 (index = 2)
            Object discountObj = tblPromotion.getValueAt(selectedRow, 2);
            if (discountObj != null) {
                try {
                    discountPercent = Double.parseDouble(discountObj.toString());
                } catch (NumberFormatException e) {
                    discountPercent = 0;
                }
            } else {
                discountPercent = 0;
            }

            updateTotalWithDiscount();
        }
    }//GEN-LAST:event_tblPromotionMouseClicked

    private void tblCartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCartMouseClicked
        int selectedRow = tblCart.getSelectedRow();
        if (selectedRow >= 0) {
            String id = tblCart.getValueAt(selectedRow, 0).toString();

            // Lấy thông tin sản phẩm từ DB
            String imagePath = productDAO.getImagePathById(id);

            if (imagePath != null && !imagePath.isEmpty()) {
                // Chuyển lại đường dẫn về đúng dạng Windows
                imagePath = imagePath.replace("\\\\", "\\");
                File imageFile = new File(imagePath);

                if (imageFile.exists()) {
                    ImageIcon icon = new ImageIcon(new ImageIcon(imageFile.getAbsolutePath())
                        .getImage().getScaledInstance(lblImage.getWidth(), lblImage.getHeight(), Image.SCALE_SMOOTH));
                    lblImage.setIcon(icon);
                    lblImage.setText("");
                } else {
                    lblImage.setIcon(null);
                    lblImage.setText("Ảnh không tồn tại");
                }
            } else {
                lblImage.setIcon(null);
                lblImage.setText("Chưa có ảnh");
            }
        }
    }//GEN-LAST:event_tblCartMouseClicked

